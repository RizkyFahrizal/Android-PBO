package com.smkrevit.kalkulator;
//nama class diawali huruf besar, nama method tidak
//didalam class isinya adalah method
//class adalah sebuah wadah yang menampung method, variabel
public class Kalkulator {
    public double penjumlahan(double a, double b){
        return a + b;
    }

    public double pengurangan(double a, double b){
        return a - b;
    }

    public double perkalian(double a, double b){
        return a * b;
    }

    public double pembagian(double a, double b){
        return a / b;
    }
}
